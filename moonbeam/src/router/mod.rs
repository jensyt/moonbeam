//! # Router Module
//!
//! This module provides the core traits and types for the routing system in Moonbeam.
//!
//! It includes:
//! - `RouteHandler`: A trait for types that can handle HTTP requests within a router.
//! - `PathParams`: A wrapper for extracting named path parameters from the URL.
//! - `FromParams`: A trait for converting raw path parameters into strongly-typed values.
//!
//! The routing system is designed to be used with the `#[route]` and `router!` macros
//! provided by the `moonbeam` crate (via `moonbeam-attributes`).

mod path_params;
pub use path_params::{FromParams, PathParams};

use crate::http::{Body, Request, Response};
use std::future::Future;

/// Trait implemented by route handlers generated by the `#[route]` macro.
///
/// This trait defines the interface for handling requests with support for:
/// - Path parameters (e.g., `/users/:id`)
/// - Rest parameters (e.g., `/static/*path`)
/// - Application state injection
///
/// Implementations of this trait are typically generated automatically by the `#[route]` macro.
#[cfg_attr(docsrs, doc(cfg(feature = "router")))]
pub trait RouteHandler<S>: 'static {
	/// Handles an incoming HTTP request.
	///
	/// # Arguments
	///
	/// * `req` - The incoming HTTP request.
	/// * `params` - A slice of path parameter values extracted from the URL.
	/// * `state` - A reference to the application state.
	fn call<'a, 'b>(
		&self,
		req: Request<'a, 'b>,
		params: &'_ [&'b str],
		state: &'static S,
	) -> impl Future<Output = impl Into<Response>>;
}

impl From<()> for Response {
	fn from(_: ()) -> Response {
		Response::empty()
	}
}

impl From<Body> for Response {
	fn from(body: Body) -> Response {
		Response::new_with_body(body, Body::DEFAULT_CONTENT_TYPE)
	}
}

impl From<(Body, &'static str)> for Response {
	fn from((body, content_type): (Body, &'static str)) -> Response {
		Response::new_with_body(body, Some(content_type))
	}
}

impl From<(Body, Option<&'static str>)> for Response {
	fn from((body, content_type): (Body, Option<&'static str>)) -> Response {
		Response::new_with_body(body, content_type)
	}
}

impl From<(Body, String)> for Response {
	fn from((body, content_type): (Body, String)) -> Response {
		Response::new_with_body(body, Some(content_type))
	}
}

impl From<(Body, Option<String>)> for Response {
	fn from((body, content_type): (Body, Option<String>)) -> Response {
		Response::new_with_body(body, content_type)
	}
}

impl<T: Into<Response>> From<Option<T>> for Response {
	fn from(val: Option<T>) -> Response {
		match val {
			Some(val) => val.into(),
			None => Response::not_found(),
		}
	}
}

impl<T: Into<Response>, E: Into<Response>> From<Result<T, E>> for Response {
	fn from(val: Result<T, E>) -> Response {
		match val {
			Ok(val) => val.into(),
			Err(err) => err.into(),
		}
	}
}
